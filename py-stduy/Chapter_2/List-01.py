# 2. 리스트 연산하기

# 리스트 더하기
a2 = [1,2,3]
b2 = [4,5,6]
print(a2+b2)        # [1, 2, 3, 4, 5, 6]

# 리스트 길이 구하기
a2 = [1,2,3]
print(len(a2))      # 3

# 리스트 반복하기
a2 = [1,2,3]
result = a2*3
print(result)       # [1, 2, 3, 1, 2, 3, 1, 2, 3]

a2 = [1,2,3]
result = str(a2[2]) + "hi"   # 자료형 일치 중요 2는 int 이므로 str로 문자열로 바꿔준다
print(result)       # 3hi
