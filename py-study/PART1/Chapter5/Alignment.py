#정렬
sentence = "a duck goes into a bar"

#가운데 정렬
print(sentence.center(50))
""">>>               a duck goes into a bar               """

#왼쪽 정렬
print(sentence.ljust(50))
""">>>a duck goes into a bar                              """

#오른쪽 정렬
print(sentence.rjust(50))
""">>>                              a duck goes into a bar"""